-- DROP
DROP TABLE IF EXISTS CHAT;
DROP TABLE IF EXISTS MESSAGE;
DROP TABLE IF EXISTS MESSAPROCESSING;
DROP TABLE IF EXISTS USERS;
DROP TABLE IF EXISTS USERINCHAT;

-- CREATE
CREATE TABLE CHAT(ID INT PRIMARY KEY AUTO_INCREMENT, NAME VARCHAR(50));
CREATE TABLE MESSAGE(ID INT PRIMARY KEY AUTO_INCREMENT, SENTBYUSERWITHID INT, TEXT VARCHAR(255), SENDINGDATE TIMESTAMP);
CREATE TABLE MESSAGEPROCESSING(ID INT PRIMARY KEY AUTO_INCREMENT, IDCHAT INT, IDMESSAGE INT);
CREATE TABLE USERS(ID INT PRIMARY KEY AUTO_INCREMENT, NAME VARCHAR(50), LOGIN VARCHAR(50), PASSWORD VARCHAR(50));
CREATE TABLE USERINCHAT(ID INT PRIMARY KEY AUTO_INCREMENT, USERID INT, CHATID INT);

